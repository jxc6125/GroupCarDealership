package Dealership;

public class Salesperson {

    private String FirstName, LastName, CompanyName, Payrate, Rank;

    public String getFirstName() {return FirstName;}
    public void setFirstName(String _FirstName) {
        this.FirstName = _FirstName;
    }
    public String getLastName() {return LastName;}
    public void setLastName(String _LastName) {this.LastName = _LastName;}
    public String getCompanyName() {return CompanyName;}
    public void setCompanyName(String _CompanyName) {this.CompanyName = _CompanyName;}
    public String getPayrate() {return Payrate;}
    public void setPayrate(String _Payrate) {this.Payrate = _Payrate;}
    public String getRank() {return Rank;}
    public void setRank(String _Rank) {this.Rank = _Rank;}
}
